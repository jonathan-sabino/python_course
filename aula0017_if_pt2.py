# if / elif / else

# o bloco de código "if" sempre para de ser executado
# ao encontrar a primeira condição verdadeira
condicao1 = False
condicao2 = True
condicao3 = False
condicao4 = False

# if pode estar sozinho, mas elif e else não
if condicao1:
    print("Código condição 1")
elif condicao2:
    print("Código condição 2")
elif condicao3:
    print("Código condição 3")
elif condicao4:
    print("Código condição 4")
else:
    print("Código nenhuma das condições")
