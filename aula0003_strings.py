"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print("Jonathan Sabino")
print(1, 'Jonathan "Sabino"')

# Aspas duplas
print("Jonathan Sabino")
print(2, "Jonathan 'Sabino'")

# Escape (pula o próximo caractere)
print('Jonathan "Sabino"')

# r (mostra também o caractere de escape) (expressões regulares)
print(r"Jonathan \"Sabino\"")
